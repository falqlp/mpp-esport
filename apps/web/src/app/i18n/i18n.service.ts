export type TranslationKey = keyof typeof en;
const translations: Record<Language, typeof en> = { en, fr, es, pt, de, it };

@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly document = inject(DOCUMENT);
  private readonly selected = localStorage.getItem('mpp-language');
  readonly language = signal<Language>(
    LANGUAGES.includes(this.selected as Language)
      ? (this.selected as Language)
      : detectLanguage(navigator.languages?.length ? navigator.languages : [navigator.language]),
  );

  constructor() {
    this.document.documentElement.lang = this.language();
  }

  setLanguage(language: Language): void {
    this.language.set(language);
    localStorage.setItem('mpp-language', language);
    this.document.documentElement.lang = language;
  }

  translate(key: TranslationKey, params: Record<string, string | number> = {}): string {
    return Object.entries(params).reduce(
      (text, [name, value]) => text.replaceAll(`{{${name}}}`, String(value)),
      translations[this.language()][key],
    );
  }
}
